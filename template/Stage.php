                <p>Pour ce stage, ma mission était de réaliser le site vitrine de l'association. J'ai eu carte
                    blanche sur les moyens à utiliser. J'ai donc décidé de créer un site en HTML, CSS avec
                    Bootstrap.</p>

                <p>Ma première tâche a été de réaliser un cahier des charges avec les besoins de l'association. Une
                    fois cela fait, j'ai demandé à un de mes amis travaillant dans le graphisme de réaliser une
                    maquette du site web.</p>

                <p>Ensuite, j'ai créé un tableau Trello récapitulant toutes les tâches que je devais réaliser, en
                    séparant les aspects techniques et stylistiques, car je n'avais pas encore la maquette à ce
                    moment-là.</p>
