                <h5>Gestion du patrimoine : </h5>

                <p>L'entreprise gère son patrimoine informatique de manière manuelle, sans utiliser d'outils dédiés.
                    Les demandes d'assistance des utilisateurs sont gérées au cas par cas, sans logiciel dédié, par
                    mail ou téléphone. La gestion des incidents et du parc informatique est également réalisée de
                    manière manuelle.</p>

                <p>L'entreprise suit les normes de programmation de Ruby on Rails et utilise Redmine pour la gestion
                    de projets, mais a testé d'autres solutions qui se sont avérées trop complexes.</p>

                <p>Les mises à jour des logiciels sont effectuées en fonction de l'évolution des projets, mais
                    l'entreprise attend une version stable avant de les appliquer. La gestion des sources des
                    projets est réalisée avec Git. Les sauvegardes sont effectuées quotidiennement avec
                    Automysqlbackup et Rdiffbackup.</p>

                <p>La formation est une composante permanente de la culture de l'entreprise, déclenchée soit par un
                    intérêt spécifique des employés, soit à la suite d'une demande client. La veille technologique
                    est également permanente au sein de l'entreprise, mais pas organisée.</p>

                <p>L'entreprise dispose d'un site web, d'un blog, d'une newsletter, ainsi que de comptes sur
                    Facebook et LinkedIn. M. Hyve, qui dispose d'un diplôme d'Administrateur Infrastructure en
                    cybersécurité, est responsable de la cybersécurité. L'entreprise applique les normes du RGPD et
                    M. Hys assume le rôle de Délégué à la Protection des Données (DPO).</p>


