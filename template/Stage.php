                    <h5 class="text-primary">Présentation du Cégep</h5>
                    <p>Le Cégep de La Pocatière est un établissement d’enseignement collégial situé dans le
                        Bas-Saint-Laurent, au Québec. Il compte environ 1000 étudiants répartis sur trois campus : La
                        Pocatière, Montmagny et Témiscouata-sur-le-Lac.</p>
                    <p>Il propose 14 programmes, dont certains uniques à l’est du Québec, ainsi que plusieurs services
                        comme des cliniques-écoles, une bibliothèque, des résidences étudiantes et un centre sportif.
                    </p>
                </section>

                <!-- Présentation du stage -->
                <section class="mb-4">
                    <h5 class="text-primary">Le Stage</h5>
                    <p>En double diplomation, notre stage devait se faire sur place, avec des horaires aménagés. Le
                        cégep propose les stages lui-même, soit pour des entreprises locales, soit pour ses propres
                        projets internes.</p>
                </section>

                <!-- Mon stage -->
                <section class="mb-4">
                    <h5 class="text-primary">Mon Stage</h5>
                    <p>J’ai travaillé sur un jeu <strong>Unity</strong> en duo destiné au <strong>mur
                            interactif</strong> du cégep, un dispositif composé d’un projecteur et d’une caméra
                        <strong>Kinect</strong> permettant de jouer avec son corp. L’objectif était de créer une
                        expérience courte, ludique et accessible entre deux cours. En sachant que c'était notre tout
                        premier projet unity n'étant pas le moteur que l'on voyais en cours.</p>
                </section>

                <!-- Présentation du jeu -->
                <section class="mb-4">
                    <h5 class="text-primary">Le Jeu</h5>
                    <p>Le joueur contrôle un <strong>vaisseau spatial</strong> à l’aide de ses mains pour éviter des
                        obstacles (astéroïdes, lasers, etc.). Le gameplay s’inspire de jeux comme <em>No Humanity</em>
                        ou <em>Minutescape</em>. Le but est simple : survivre le plus longtemps possible.</p>
                    <p>Un <strong>classement des meilleurs scores</strong> est intégré afin de motiver les joueurs à
                        rejouer et se dépasser.</p>
                </section>

                <!-- Contraintes et solution -->
                <section class="mb-4">
                    <h5 class="text-primary">Contraintes scolaires</h5>
                    <p>Pour éviter les abus dans les pseudos du tableau de scores, <strong>j’ai proposé</strong> une
                        solution : chaque joueur choisit une <strong>icône</strong> et indique sa
                        <strong>filière</strong>. Cela évite les contenus inappropriés et encourage une saine
                        compétition entre programmes.</p>
                    <p>À ce jour, c’est le <strong>seul jeu sur le mur interactif</strong> avec un système de classement
                        intégré.</p>
                </section>

