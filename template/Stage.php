                <h5>Gestion du patrimoine : </h5>



                <p>L'Association Sportive Omnisports de Bourgueil (ESB) dispose d'un parc informatique composé de 4
                    ordinateurs et une tablette, achetés auprès de l'entreprise partenaire Loire Informatique.</p>

                <ul>
                    <li>Les ordinateurs sont répartis entre les différentes sections de l'association :
                        <ul>
                            <li>un pour l'ESB,</li>
                            <li>un pour le basket,</li>
                            <li>un pour le patinage artistique et</li>
                            <li>un pour le football, qui dispose également d'une tablette fournie par la FFF.</li>
                        </ul>
                    </li>
                    <li>Les autres sections doivent apporter leur propre matériel, mais l'association a pour
                        objectif d'agrandir son parc informatique pour que chaque section puisse disposer d'un
                        équipement dédié.</li>
                    <li>Le président de l'ESB est responsable de la gestion du patrimoine informatique, mais les
                        sections sont chargées de gérer leurs propres postes, notamment les mises à jour.</li>
                    <li>En cas de problème technique, l'association fait appel à son entreprise partenaire Loire
                        Informatique.</li>
                </ul>

